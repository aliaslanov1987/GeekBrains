a = int(input())

if a < 60:
    print(a, 'сек')
elif a < 3600:
    b = a // 60
    c = a % 60
    print(b, 'мин', c, 'сек')
elif a < 86400:
    b = a // 3600
    c = (a % 3600) // 60
    d = (a % 3600) % 60
    print(b, 'час', c, 'мин', d, 'сек')
else:
    b = a // 86400
    c = a % 86400 // 3600
    d = a % 86400 % 3600 // 60
    e = a % 86400 % 3600 % 60
    print(b, 'дн', c, 'час', d, 'мин', e, 'сек')